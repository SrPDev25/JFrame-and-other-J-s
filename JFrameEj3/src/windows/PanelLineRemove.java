    private void chargeLines() {
        modelCombo.removeAllElements();
        modelCombo.addAll(miControl.getLines());
        if (modelCombo.getSize() == 0) {
            lblError.setText("No hay lineas en el sistema");
        } else {
            lblError.setText("");

        }
    }

